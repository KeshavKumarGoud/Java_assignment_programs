package Automation_class_16;

public class String_substring {
	
	public static void main(String[] args)
	{
		String name= "Manish Kumar Tiwari";
		System.out.println("First substring is:" + name.substring(0, 6));
		System.out.println("First substring is:" + name.substring(7, 12));
		System.out.println("First substring is:" + name.substring(13));
	}

}
